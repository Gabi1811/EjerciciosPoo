package Tarea2511;

public class Operaciones {

	public int resultado, a, b;
	{
		a = 10;
		b = 5;
	}

	public int sumar() {
		resultado = a + b;
		return resultado;
	};

	public int multiplicar() {
		resultado = a * b;
		return resultado;
	};

	public int resta() {
		resultado = a - b;
		return resultado;
	};
}

